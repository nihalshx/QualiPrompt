"""
dataset_publisher.py
────────────────────
Publish the accumulated session_history to Hugging Face Datasets.

The dataset is uploaded as a parquet file with a README (DatasetCard)
describing the schema and methodology.
"""

from __future__ import annotations

import os
import textwrap
from pathlib import Path
from typing import Optional

import pandas as pd


_DATASET_CARD = textwrap.dedent("""
    ---
    language:
    - en
    license: mit
    task_categories:
    - text-generation
    tags:
    - prompt-engineering
    - evaluation
    - llm
    - gemini
    - quality-metrics
    size_categories:
    - n<1K
    ---

    # QualiPrompt Dataset

    A systematically collected, annotated dataset of prompt–response pairs
    generated by the **QualiPrompt** framework (*Quality-driven prompt evaluation, scientifically.*).

    ## Overview

    Each row corresponds to one LLM call: a specific *task description* was
    submitted using one of five canonical prompt-engineering strategies, and
    the resulting response was automatically scored on three quality axes.

    ## Schema

    | Column | Type | Description |
    |--------|------|-------------|
    | `session_id` | string | UUID grouping all five variants for one task |
    | `timestamp` | string | ISO-8601 UTC timestamp |
    | `task` | string | Original natural-language task description |
    | `strategy` | string | Prompt strategy identifier |
    | `strategy_label` | string | Human-readable strategy name |
    | `prompt` | string | Full prompt text sent to the model |
    | `response` | string | Model response text |
    | `semantic_relevance` | float | Cosine similarity (task ↔ response), 0–10 |
    | `length_score` | float | Word-count appropriateness score, 0–10 |
    | `readability_score` | float | Flesch Reading Ease normalised to 0–10 |
    | `final_score` | float | Weighted composite quality score, 0–10 |
    | `word_count` | int | Word count of response |
    | `flesch_reading_ease` | float | Raw Flesch Reading Ease value |
    | `latency_s` | float | API call latency in seconds |
    | `model` | string | LLM model name (e.g. `gemini-1.5-flash`) |
    | `error` | string | Error message if the call failed, else null |

    ## Prompt Strategies

    | Strategy | Description |
    |----------|-------------|
    | `zero_shot` | Direct task restatement — no examples or scaffolding |
    | `few_shot` | Two illustrative examples prepended |
    | `chain_of_thought` | Step-by-step reasoning instruction appended |
    | `role_play` | Domain-expert persona assigned |
    | `structured_output` | Output format specification appended |

    ## Quality Metrics

    - **Semantic Relevance (50%)** — cosine similarity between the
      `all-MiniLM-L6-v2` embeddings of the task and the response.
    - **Length Score (20%)** — word count compared to task-type norms.
    - **Readability Score (30%)** — Flesch Reading Ease normalised so that
      professional-range text (40–70) scores 10/10.

    ## Citation

    ```bibtex
    @dataset{prompt_quality_dataset,
      title   = {QualiPrompt Dataset},
      author  = {QualiPrompt},
      year    = {2024},
      url     = {https://huggingface.co/datasets/YOUR_REPO_HERE},
      license = {MIT}
    }
    ```
""").strip()


def publish_to_huggingface(
    df: pd.DataFrame,
    repo_id: str,
    hf_token: str,
    private: bool = False,
) -> str:
    """
    Upload *df* to the Hugging Face Hub as a parquet dataset.

    Returns the dataset URL.
    Raises RuntimeError on failure.
    """
    try:
        from datasets import Dataset
        from huggingface_hub import HfApi
    except ImportError as exc:
        raise RuntimeError(
            "Install 'datasets' and 'huggingface-hub' to use this feature."
        ) from exc

    if df.empty:
        raise ValueError("Nothing to publish: history is empty.")

    # Convert to HF Dataset
    hf_dataset = Dataset.from_pandas(df.reset_index(drop=True))

    # Push data
    hf_dataset.push_to_hub(
        repo_id=repo_id,
        token=hf_token,
        private=private,
    )

    # Upload README / DatasetCard
    api = HfApi(token=hf_token)
    readme_path = Path("/tmp/README.md")
    readme_path.write_text(_DATASET_CARD, encoding="utf-8")
    api.upload_file(
        path_or_fileobj=str(readme_path),
        path_in_repo="README.md",
        repo_id=repo_id,
        repo_type="dataset",
        token=hf_token,
    )

    return f"https://huggingface.co/datasets/{repo_id}"
